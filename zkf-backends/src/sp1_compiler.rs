/// Generate a minimal guest Cargo.toml for compiled mode.
/// No zkf-core dependency needed — constraints are baked into the source.
pub fn compiled_guest_cargo_toml(target: GuestTarget) -> String {
    match target {
        GuestTarget::Sp1 => {
            r#"[package]
name = "zkf_sp1_guest"
version = "0.1.0"
edition = "2021"

[dependencies]
sp1-zkvm = "6.0.2"
serde = { version = "1", features = ["derive"] }

# SP1 Precompile Acceleration (available if BlackBox ops need crypto)
[patch.crates-io]
sha2 = { git = "https://github.com/sp1-patches/RustCrypto-hashes", package = "sha2", branch = "patch-sha2-v0.10.8" }
tiny-keccak = { git = "https://github.com/sp1-patches/tiny-keccak", branch = "patch-v2.0.2" }
"#
            .to_string()
        }
        GuestTarget::RiscZero => {
            r#"[package]
name = "zkf_risc0_guest"
version = "0.1.0"
edition = "2021"

[dependencies]
risc0-zkvm = { version = "2.2", default-features = false, features = ["guest"] }
serde = { version = "1", features = ["derive"] }
"#
            .to_string()
        }
    }
}

/// Check if a program is suitable for native compilation.
///
/// Returns Ok(()) if the program can be fully compiled, or Err with reason.
pub fn check_compilable(program: &Program) -> ZkfResult<()> {
    // Field must be one of the small fields we support
    if !matches!(
        program.field,
        FieldId::Goldilocks | FieldId::BabyBear | FieldId::Mersenne31
    ) {
        return Err(ZkfError::Backend(format!(
            "native compilation requires Goldilocks/BabyBear/Mersenne31 field, got {}",
            program.field.as_str()
        )));
    }

    // Check for BlackBox constraints that need host validation
    let has_blackbox = program.constraints.iter().any(|c| matches!(c,
        Constraint::BlackBox { op, .. } if *op != BlackBoxOp::RecursiveAggregationMarker
    ));

    if has_blackbox {
        // Programs with BlackBox constraints can still use compiled mode,
        // but the host must pre-validate BlackBox constraints via check_constraints().
        // This is already the case (the host calls check_constraints before proving).
    }

    Ok(())
}

