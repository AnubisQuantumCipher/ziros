#[cfg(test)]
mod tests {
    use super::*;
    use zkf_core::{FieldElement, Signal, Visibility};

    fn make_multiply_program(field: FieldId) -> Program {
        Program {
            name: "multiply".to_string(),
            field,
            signals: vec![
                Signal {
                    name: "x".to_string(),
                    visibility: Visibility::Private,
                    constant: None,
                    ty: None,
                },
                Signal {
                    name: "y".to_string(),
                    visibility: Visibility::Private,
                    constant: None,
                    ty: None,
                },
                Signal {
                    name: "out".to_string(),
                    visibility: Visibility::Public,
                    constant: None,
                    ty: None,
                },
            ],
            constraints: vec![Constraint::Equal {
                lhs: Expr::Signal("out".to_string()),
                rhs: Expr::Mul(
                    Box::new(Expr::Signal("x".to_string())),
                    Box::new(Expr::Signal("y".to_string())),
                ),
                label: Some("out = x * y".to_string()),
            }],
            witness_plan: Default::default(),
            ..Default::default()
        }
    }

    #[test]
    fn compile_multiply_goldilocks_sp1() {
        let program = make_multiply_program(FieldId::Goldilocks);
        let code = compile_to_guest_rust(&program, GuestTarget::Sp1).unwrap();
        assert!(code.contains("FIELD_P"));
        assert!(code.contains("field_mul"));
        assert!(code.contains("check_compiled"));
        assert!(code.contains("sp1_zkvm"));
        assert!(code.contains("w[2]")); // out signal
        assert!(code.contains("field_mul(w[0], w[1])"));
    }

    #[test]
    fn compile_multiply_babybear_risc0() {
        let program = make_multiply_program(FieldId::BabyBear);
        let code = compile_to_guest_rust(&program, GuestTarget::RiscZero).unwrap();
        assert!(code.contains("0x78000001")); // BabyBear prime
        assert!(code.contains("risc0_zkvm"));
        assert!(code.contains("check_compiled"));
    }

    #[test]
    fn compile_rejects_bn254() {
        let program = make_multiply_program(FieldId::Bn254);
        let result = compile_to_guest_rust(&program, GuestTarget::Sp1);
        assert!(result.is_err());
        let msg = result.unwrap_err().to_string();
        assert!(msg.contains("does not yet support"));
    }

    #[test]
    fn compile_boolean_and_range_constraints() {
        let program = Program {
            name: "mixed".to_string(),
            field: FieldId::Goldilocks,
            signals: vec![
                Signal {
                    name: "flag".to_string(),
                    visibility: Visibility::Private,
                    constant: None,
                    ty: None,
                },
                Signal {
                    name: "val".to_string(),
                    visibility: Visibility::Private,
                    constant: None,
                    ty: None,
                },
            ],
            constraints: vec![
                Constraint::Boolean {
                    signal: "flag".to_string(),
                    label: None,
                },
                Constraint::Range {
                    signal: "val".to_string(),
                    bits: 8,
                    label: Some("val < 256".to_string()),
                },
            ],
            witness_plan: Default::default(),
            ..Default::default()
        };

        let code = compile_to_guest_rust(&program, GuestTarget::Sp1).unwrap();
        assert!(code.contains("w[0] != 0 && w[0] != 1")); // boolean
        assert!(code.contains("w[1] >= 256")); // range 8-bit
    }

    #[test]
    fn compile_add_sub_div_expressions() {
        let program = Program {
            name: "arith".to_string(),
            field: FieldId::Mersenne31,
            signals: vec![
                Signal { name: "a".to_string(), visibility: Visibility::Private, constant: None, ty: None },
                Signal { name: "b".to_string(), visibility: Visibility::Private, constant: None, ty: None },
                Signal { name: "c".to_string(), visibility: Visibility::Public, constant: None, ty: None },
            ],
            constraints: vec![
                Constraint::Equal {
                    lhs: Expr::Signal("c".to_string()),
                    rhs: Expr::Add(vec![
                        Expr::Signal("a".to_string()),
                        Expr::Signal("b".to_string()),
                        Expr::Const(FieldElement::from_i64(5)),
                    ]),
                    label: None,
                },
            ],
            witness_plan: Default::default(),
            ..Default::default()
        };

        let code = compile_to_guest_rust(&program, GuestTarget::Sp1).unwrap();
        assert!(code.contains("field_add"));
        assert!(code.contains("5u64"));
    }

    #[test]
    fn check_compilable_accepts_small_fields() {
        for field in [FieldId::Goldilocks, FieldId::BabyBear, FieldId::Mersenne31] {
            let program = make_multiply_program(field);
            assert!(check_compilable(&program).is_ok());
        }
    }

    #[test]
    fn check_compilable_rejects_bn254() {
        let program = make_multiply_program(FieldId::Bn254);
        assert!(check_compilable(&program).is_err());
    }

    #[test]
    fn compiled_cargo_toml_has_no_zkf_core() {
        let toml = compiled_guest_cargo_toml(GuestTarget::Sp1);
        assert!(!toml.contains("zkf-core"));
        assert!(toml.contains("sp1-zkvm"));
    }
}
