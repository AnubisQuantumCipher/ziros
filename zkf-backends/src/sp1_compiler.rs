// ---------------------------------------------------------------------------
// Constraint code generation
// ---------------------------------------------------------------------------

fn emit_constraints(
    program: &Program,
    signal_map: &BTreeMap<String, usize>,
) -> ZkfResult<String> {
    let mut lines = Vec::new();
    for (i, constraint) in program.constraints.iter().enumerate() {
        match constraint {
            Constraint::Equal { lhs, rhs, label } => {
                let lhs_code = emit_expr(lhs, signal_map)?;
                let rhs_code = emit_expr(rhs, signal_map)?;
                let comment = label
                    .as_ref()
                    .map(|l| format!(" // {l}"))
                    .unwrap_or_default();
                lines.push(format!(
                    "    // Constraint {i}: Equal{comment}\n    \
                     {{\n        \
                         let lhs = {lhs_code};\n        \
                         let rhs = {rhs_code};\n        \
                         if lhs != rhs {{ return false; }}\n    \
                     }}"
                ));
            }
            Constraint::Boolean { signal, label } => {
                let idx = signal_map.get(signal).ok_or_else(|| {
                    ZkfError::Backend(format!("unknown signal '{signal}' in boolean constraint"))
                })?;
                let comment = label
                    .as_ref()
                    .map(|l| format!(" // {l}"))
                    .unwrap_or_default();
                lines.push(format!(
                    "    // Constraint {i}: Boolean{comment}\n    \
                     if w[{idx}] != 0 && w[{idx}] != 1 {{ return false; }}"
                ));
            }
            Constraint::Range { signal, bits, label } => {
                let idx = signal_map.get(signal).ok_or_else(|| {
                    ZkfError::Backend(format!("unknown signal '{signal}' in range constraint"))
                })?;
                let comment = label
                    .as_ref()
                    .map(|l| format!(" // {l}"))
                    .unwrap_or_default();
                if *bits >= 64 {
                    // Always passes for u64 representation
                    lines.push(format!(
                        "    // Constraint {i}: Range({bits}){comment} — always true for u64"
                    ));
                } else {
                    let bound = 1u64 << bits;
                    lines.push(format!(
                        "    // Constraint {i}: Range({bits}){comment}\n    \
                         if w[{idx}] >= {bound} {{ return false; }}"
                    ));
                }
            }
            Constraint::BlackBox { op, label, .. } => {
                // BlackBox constraints are NOT compiled — they require the full
                // zkf-core enrichment/validation pipeline. The compiled path
                // handles algebraic constraints; BlackBox ops are validated
                // separately by the host before proving (via check_constraints).
                //
                // RecursiveAggregationMarker is metadata-only and always passes.
                if *op == BlackBoxOp::RecursiveAggregationMarker {
                    lines.push(format!(
                        "    // Constraint {i}: BlackBox(RecursiveAggregationMarker) — metadata only"
                    ));
                } else {
                    let comment = label
                        .as_ref()
                        .map(|l| format!(" // {l}"))
                        .unwrap_or_default();
                    lines.push(format!(
                        "    // Constraint {i}: BlackBox({op}){comment}\n    \
                         // NOTE: BlackBox constraint validated by host via check_constraints();\n    \
                         // the compiled guest path trusts the host's pre-validation.",
                        op = op.as_str()
                    ));
                }
            }
            Constraint::Lookup { table, label, .. } => {
                let comment = label
                    .as_ref()
                    .map(|l| format!(" // {l}"))
                    .unwrap_or_default();
                lines.push(format!(
                    "    // Constraint {i}: Lookup(table={table}){comment}\n    \
                     // NOTE: Lookup constraint must be lowered before compilation;\n    \
                     // if it reaches here, the host validates it via check_constraints()."
                ));
            }
        }
    }
    Ok(lines.join("\n"))
}

fn emit_expr(expr: &Expr, signal_map: &BTreeMap<String, usize>) -> ZkfResult<String> {
    match expr {
        Expr::Const(fe) => {
            // Emit the field element as a u64 literal.
            // FieldElement stores LE bytes; extract up to 8 bytes as u64.
            let le_bytes = fe.to_le_bytes();
            let mut buf = [0u8; 8];
            let copy_len = le_bytes.len().min(8);
            buf[..copy_len].copy_from_slice(&le_bytes[..copy_len]);
            let val = u64::from_le_bytes(buf);
            Ok(format!("{val}u64"))
        }
        Expr::Signal(name) => {
            let idx = signal_map.get(name).ok_or_else(|| {
                ZkfError::Backend(format!("unknown signal '{name}' in expression"))
            })?;
            Ok(format!("w[{idx}]"))
        }
        Expr::Add(terms) => {
            if terms.is_empty() {
                return Ok("0u64".to_string());
            }
            if terms.len() == 1 {
                return emit_expr(&terms[0], signal_map);
            }
            let first = emit_expr(&terms[0], signal_map)?;
            let mut code = first;
            for term in &terms[1..] {
                let t = emit_expr(term, signal_map)?;
                code = format!("field_add({code}, {t})");
            }
            Ok(code)
        }
        Expr::Sub(a, b) => {
            let a_code = emit_expr(a, signal_map)?;
            let b_code = emit_expr(b, signal_map)?;
            Ok(format!("field_sub({a_code}, {b_code})"))
        }
        Expr::Mul(a, b) => {
            let a_code = emit_expr(a, signal_map)?;
            let b_code = emit_expr(b, signal_map)?;
            Ok(format!("field_mul({a_code}, {b_code})"))
        }
        Expr::Div(a, b) => {
            let a_code = emit_expr(a, signal_map)?;
            let b_code = emit_expr(b, signal_map)?;
            Ok(format!("field_div({a_code}, {b_code})"))
        }
    }
}

// ---------------------------------------------------------------------------
// Entrypoint generation
// ---------------------------------------------------------------------------

fn emit_sp1_entrypoint(witness_count: usize, digest: &str) -> String {
    format!(
        r#"
pub fn main() {{
    // Read witness values (flat u64 array)
    let witness: Vec<u64> = sp1_zkvm::io::read();
    assert_eq!(witness.len(), {witness_count}, "witness length mismatch");

    // Evaluate compiled constraints
    let ok = check_compiled(&witness);

    // Commit (digest, satisfied) — digest is baked in at compile time
    sp1_zkvm::io::commit(&("{digest}".to_string(), ok));
}}
"#
    )
}

fn emit_risc0_entrypoint(witness_count: usize, digest: &str) -> String {
    format!(
        r#"
fn main() {{
    // Read witness values (flat u64 array)
    let witness: Vec<u64> = risc0_zkvm::guest::env::read();
    assert_eq!(witness.len(), {witness_count}, "witness length mismatch");

    // Evaluate compiled constraints
    let ok = check_compiled(&witness);

    // Commit (digest, satisfied) — digest is baked in at compile time
    risc0_zkvm::guest::env::commit(&("{digest}".to_string(), ok));
}}
"#
    )
}

