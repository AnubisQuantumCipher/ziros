              | Result_Err (error) =>
                Result_Err (error)
              end
            | Option_None =>
              let expected_outputs := Option_None in
              if
                lookup_has_matching_row_from (f_deref (f_rows lookup_table)) (f_deref (evaluated_inputs)) (expected_outputs) (impl_1__len (inputs)) (f_field program)
