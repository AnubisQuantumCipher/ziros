          end
      | Option_None =>
        Result_Err (SpecKernelCheckError_UnknownLookupTable {| SpecKernelCheckError_UnknownLookupTable_f_table_index := (table_index) |})
      end
    end
  | Option_None =>
    Result_Ok (tt)
  end.

